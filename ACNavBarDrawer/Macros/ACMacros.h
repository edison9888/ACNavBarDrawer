/** DEBUG LOG **/
#ifdef DEBUG

#define DLog(format, ...) do {                                                                             \
fprintf(stderr, "\n//------------------------------\n");                    \
fprintf(stderr, "<%s : %d> %s\n",                                           \
[[[NSString stringWithUTF8String:__FILE__] lastPathComponent] UTF8String],  \
__LINE__, __func__);                                                        \
(NSLog)((format), ##__VA_ARGS__);                                           \
fprintf(stderr, "//----------------------------//\n");                      \
} while (0)

#else

#define DLog( ... )

#endif


